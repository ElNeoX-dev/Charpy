public class Pendule {

    double masse;
    double longueur;
    double angleInitial;
    double vitesseInitiale;

    public Pendule (double uneMasse, double uneLongueur, double unAngleInitial, double uneVitesseInitiale) {
        this.masse=uneMasse;
        this.longueur=uneLongueur;
        this.angleInitial=unAngleInitial;
        this.vitesseInitiale=uneVitesseInitiale;
    }
}